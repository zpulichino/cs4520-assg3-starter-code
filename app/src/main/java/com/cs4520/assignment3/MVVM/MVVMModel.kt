package com.cs4520.assignment3.MVVM

class MVVMModel {

    fun add(a: Double, b: Double): Double {
        return a + b
    }

    fun sub(a: Double, b: Double): Double {
        return a - b
    }

    fun multiply(a: Double, b: Double): Double {
        return a * b
    }

    fun div(a: Double, b: Double): Double {
        return a / b
    }




}